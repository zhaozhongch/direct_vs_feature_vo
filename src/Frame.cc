cv::Mat Frame::CalculateImagePyrNext(int level){
    cv::Mat next_level(h_pyr_[level+1], w_pyr_[level+1], CV_32FC1);
    cv::Mat cl = image_pyr_[level];//current level
    //std::cout<<"calculate pyramid, next level width and height, current level height and width "<<cl.rows<<","<<cl.cols<<","<<next_level.rows<<","<<next_level.cols<<std::endl;
    //int counter = 0;
    for(int i = 0; i < next_level.rows; i++)
        for(int j = 0; j < next_level.cols; j++){
            if(2*i + 1 > cl.rows -1 || 2*j + 1 > cl.cols-1){
                //counter++;
                next_level.ptr<float>(i)[j] = cl.ptr<float>(2*i)[2*j];
            }
            next_level.ptr<float>(i)[j] = 0.25 * (cl.ptr<float>(2*i)[2*j] + cl.ptr<float>(2*i+1)[2*j] 
                                            + cl.ptr<float>(2*i)[2*j+1] + cl.ptr<float>(2*i+1)[2*j+1]);                
        }
    //std::cout<<"in level "<<level<<" there are "<<counter<<"special points"<<std::endl;
    return next_level;
};

cv::Mat Frame::CalculateDepthPyrNext(int level){
    cv::Mat next_level(h_pyr_[level+1], w_pyr_[level+1], CV_32FC1);
    cv::Mat cl = depth_pyr_[level];;
    for(int i = 0; i < next_level.rows; i++)
        for(int j = 0; j < next_level.cols; j++){
            if(2*i + 1 > cl.rows -1 || 2*j + 1 > cl.cols-1){
                next_level.ptr<float>(i)[j] = cl.ptr<float>(2*i)[2*j];
            }
            float d_counter = 4;
            if(cl.ptr<float>(2*i)[2*j] == 0) d_counter--;
            if(cl.ptr<float>(2*i+1)[2*j] == 0) d_counter--;
            if(cl.ptr<float>(2*i)[2*j+1] == 0) d_counter--;
            if(cl.ptr<float>(2*i+1)[2*j+1] == 0) d_counter--;
            if(d_counter != 0)    
                next_level.ptr<float>(i)[j] = 1.0/d_counter * (cl.ptr<float>(2*i)[2*j] + cl.ptr<float>(2*i+1)[2*j] 
                                            + cl.ptr<float>(2*i)[2*j+1] + cl.ptr<float>(2*i+1)[2*j+1]);
            else
                next_level.ptr<float>(i)[j] = 0.0;
        }
    return next_level;
};

